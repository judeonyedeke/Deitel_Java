package JavaClassWork;

import java.util.Scanner;

public class SquareSumAndDiff {

        public static int squareRoot (int num1){
            return num1 * num1;
        }

        public static int numberDifference (int num1, int num2){
            return num1 - num2;
        }

        public static int sumSquare (int num1, int num2){
            return num1 + num2;
        }
    }


